 /* Define variables for different data types int, boolean, char, float, double and 
print on the console */

package java_basics;

public class DataTypes {
    static int integer = 100;
    static boolean bool = true;
    static char character = 'A';
    static float decimal = 1.07f;
    static double dDecimal = 1.6666667d;

    public static void main(String[] args) {
        System.out.println("This is an interger: " + integer);
        System.out.println("This is a boolean: " + bool);
        System.out.println("This is a character: " + character);
        System.out.println("This is a float: " + decimal);
        System.out.println("This is a double: " + dDecimal);
    }

}
